//! \brief Non-Volatile Memory interface.
class INVMem {
public:
    virtual ~INVMem() = default;

    virtual std::size_t GetNVMemSize() const = 0;
    virtual void RdFromNVMem(
        std::span<std::byte> aData,
        std::size_t aOffset
    ) = 0;
    virtual void WrToNVMem(
        std::span<std::byte const> aData,
        std::size_t aOffset
    ) = 0;
};

